package gunGame;

public class Question {

	private int angle; 
	private int answer; 
	
	public Question(int angle, int answer)
	{
		this.angle = angle; 
		this.answer = answer;
	}

	public int getAngle() {
		return angle;
	}

	public void setAngle(int angle) {
		this.angle = angle;
	}

	public int getAnswer() {
		return answer;
	}

	public void setAnswer(int answer) {
		this.answer = answer;
	}
	
	
	
}
